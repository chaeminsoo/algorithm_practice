x1,x2 = input().split()

u1 = [[-0.3,1.0,1.2],[1.6,-1.0,-1.1]]
u2 = [[1.0,1.0,-1.0],[0.7,0.5,1.0]]
u3 = [[0.5,-0.8,1.0],[-0.1,0.3,0.4]]
u4 = [[1.0,0.1,-0.2],[-0.2,1.3,-0.4]]

u = [u1,u2,u3,u4]

for i in range(4):
    un = u[i]
    z1 = un[0][0] + u[0][1]*x1 +u[0][2]*x2
    z2 = un[1][0] + u[1][1]*x1 +u[1][2]*x2

    x1 = z1
    x2 = z2

print(x1,x2)